#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;
	size_t	j;

	j = 0;
	if ((ret = malloc(ft_strlen((char *)s1) + 1)) == NULL)
		return (0);
	while (s1[j] != '\0')
	{
		ret[j] = s1[j];
		j++;
	}
	ret[j] = '\0';
	return (ret);
}
